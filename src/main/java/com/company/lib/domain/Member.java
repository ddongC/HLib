package com.company.lib.domain;

public class Member {
	int stdID;
	int grade;
	int point;
	int rateFee;
	
	public int getStdID() {
		return stdID;
	}
	
	public void setStdID(int stdID) {
		this.stdID = stdID;
	}
	
	public int getGrade() {
		return grade;
	}
	
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
	public int getPoint() {
		return point;
	}
	
	public void setPoint(int point) {
		this.point = point;
	}
	
	public int getRateFee() {
		return rateFee;
	}
	
	public void setRateFee(int rateFee) {
		this.rateFee = rateFee;
	}
}
